#include "f.h"
/**
 * add - adds two numbers
 * @a: variable 1
 * @b: variable 2
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * mul - multiply two digits
 * @a: variable 1
 * @b: variable 2
 * Return: product of two integers
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * sub - substracts two ints
 * @a: variable 1
 * @b: variable 2
 * Return: value after subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - divides two digits
 * @a: variable 1
 * @b: variable 2
 * Return: value after division
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulus of two digits
 * @a: variable 1
 * @b: variable 2
 * Return: value after modulus
 */
int mod(int a, int b)
{
	return (a % b);
}
